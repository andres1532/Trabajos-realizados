    public void cargarDatos() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setRowCount(0);
        Favorito cone = new Favorito();
        rs = cone.TraerDatosFavorito(" SELECT cuenta.Usuario, gimnasios.NombreGimnasio FROM ((cuenta inner join favoritos on cuenta.IdUsuario =favoritos.IdUsuario) inner join gimnasios  on favoritos.IdGimnasio = gimnasios.IdGimnasio)");
        try {
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                
                

                modelo.addRow(v);
                jTable2.setModel(modelo);

            }

        } catch (SQLException ex) {

        }

    }

