    void consulta() throws SQLException, IOException{
    ImagenesEjercicios cone = new ImagenesEjercicios();
    rs = cone.traerDatosImagenesEjer("select DescripcionImagen, imagen from imagen where IdImagen='"+this.jTextField1.getText()+"'");
    
        while(rs.next()){
            this.jLabel2.setText(rs.getString(1));
            
            Blob foto = rs.getBlob(2);
            byte []datos = foto.getBytes(1, (int) foto.length());
            BufferedImage img = ImageIO.read(new ByteArrayInputStream(datos));
            
            Image image = img.getScaledInstance(jLabel1.getWidth(),jLabel1.getHeight(), Image.SCALE_SMOOTH);
            jLabel1.setIcon(new ImageIcon(image));
                    
        }
    }

